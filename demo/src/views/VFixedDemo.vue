<template>
  <div>
    <el-alert type="warning" title='自定义固定列用法: <el-table :headerCellStyle="headerCellFixedStyle" :cellStyle="cellFixedStyle"></table>设置固定列样式， 再设置固定列 <virtual-column vfixed="left/right"></virtual-column>，固定列是<virtual-column>组件内部实现，不会生成额外的table，滚动会更丝滑' show-icon></el-alert>
    <virtual-scroll
      ref="virtualScroll"
      :data="list"
      :item-size="56"
      key-prop="id"
      :selection-sort="null"
      @selection-change="updateSelection"
      @change="(virtualList) => tableData = virtualList">
      <template slot-scope="{ headerCellFixedStyle, cellFixedStyle }">
        <el-table
          :data="tableData"
          :headerCellStyle="headerCellFixedStyle"
          :cellStyle="cellFixedStyle"
          border
          stripe
          row-key="id"
          height="600px"
          style="width: 100%"
          @header-dragend="onHeaderDragend">
          <VirtualColumn
            vfixed
            type="selection"
            min-width="60">
          </VirtualColumn>
          <VirtualColumn
            v-if="show"
            vfixed
            prop="date"
            label="日期"
            min-width="180">
          </VirtualColumn>
          <VirtualColumn
            vfixed
            prop="name"
            label="姓名"
            :formatter="formatter"
            min-width="150">
          </VirtualColumn>
          <el-table-column
            prop="province"
            label="省份"
            width="150">
          </el-table-column>
          <el-table-column
            prop="province"
            label="省份"
            width="200">
          </el-table-column>
          <el-table-column
            class-name="province"
            prop="province"
            label="省份"
            width="200">
          </el-table-column>
          <el-table-column
            className="city"
            prop="city"
            label="市区"
            width="320">
          </el-table-column>
          <el-table-column
            prop="address"
            label="地址"
            width="300">
          </el-table-column>
          <VirtualColumn
            vfixed="right"
            prop="zip"
            label="邮编"
            width="150">
          </VirtualColumn>
          <VirtualColumn
            vfixed="right"
            label="操作"
            width="120">
            <template slot-scope="scope">
              <el-button @click="handleClick(scope.row)" type="text" size="small">查看</el-button>
              <el-button type="text" size="small">编辑</el-button>
              <el-button class="danger" type="text" @click="handleDelete(scope.row)" size="small">删除</el-button>
            </template>
          </VirtualColumn>
        </el-table>
      </template>
    </virtual-scroll>
    <!-- <el-button @click="show = !show">{{ show ? '隐藏' : '显示'}}第一列</el-button> -->
  </div>
</template>

<script>
import VirtualScroll, { VirtualColumn } from 'el-table-virtual-scroll'
import { mockData } from '@/utils'

export default {
  components: {
    VirtualScroll,
    VirtualColumn
  },
  data () {
    return {
      list: mockData(0, 2000),
      tableData: [],
      show: true,
      selection: [],
    }
  },
  methods: {
    onHeaderDragend () {
      // 使用自定义列，改变列宽度后，需要手动更新table头部
      this.$refs.virtualScroll.doHeaderLayout()
    },
    formatter(row, column, value) {
      return `姓名：${value}`
    },
    updateSelection(selection) {
      console.log('selection', selection)
      this.selection = selection
    },
    handleDelete(row) {
      const indexToDelete = this.list.findIndex(data => data === row)
      this.list.splice(indexToDelete, 1)
    }
  },
  created () {

  }
}
</script>

<style lang='less' scoped>
  .danger {
    color: red;
  }
</style>
